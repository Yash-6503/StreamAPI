package com.classes;

public class VarKeyword {
    public static void main(String[] args) {
        var x = 12;
        System.out.println(x);

        x = 90;
        System.out.println(x);

        // x = "NIT"; //Invalid

        var name = "Yash";
        System.out.println(name);

        // name = 20;

    }
}
